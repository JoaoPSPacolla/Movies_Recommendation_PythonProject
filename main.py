import pandas as pd
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.metrics.pairwise import cosine_similarity

# Carregar o dataset a partir do arquivo Excel
df = pd.read_excel('dados_filmes_limpo_e_focado.xlsx')

# Preencher valores NaN (nulos) com strings vazias
for feature in ['GENRES', 'KEYWORDS', 'TAGLINE', 'OVERVIEW']:
    df[feature] = df[feature].fillna('')

# Criar uma nova coluna combinando as caracter√≠sticas textuais
df['combined_features'] = df['GENRES'] + ' ' + df['KEYWORDS'] + ' ' + df['TAGLINE'] + ' ' + df['OVERVIEW']

# Criar o vetorizador TF-IDF e remover stopwords em ingl√™s
tfidf = TfidfVectorizer(stop_words='english')

# Construir a matriz TF-IDF
tfidf_matrix = tfidf.fit_transform(df['combined_features'])

# Calcular a matriz de similaridade de cosseno
cosine_sim = cosine_similarity(tfidf_matrix, tfidf_matrix)

# Criar um mapeamento de t√≠tulos de filmes para √≠ndices do DataFrame
indices = pd.Series(df.index, index=df['TITLE']).drop_duplicates()

def get_recommendations(title, cosine_sim=cosine_sim):
    """
    Fun√ß√£o que recebe o t√≠tulo de um filme e retorna uma lista de filmes recomendados.
    """
    # Obter o √≠ndice do filme que corresponde ao t√≠tulo
    idx = indices[title]

    # Obter as pontua√ß√µes de similaridade de todos os filmes com aquele filme
    sim_scores = list(enumerate(cosine_sim[idx]))

    # Ordenar os filmes com base nas pontua√ß√µes de similaridade
    sim_scores = sorted(sim_scores, key=lambda x: x[1], reverse=True)

    # Obter as pontua√ß√µes dos 10 filmes mais similares (excluindo o pr√≥prio filme)
    sim_scores = sim_scores[1:11]

    # Obter os √≠ndices dos filmes
    movie_indices = [i[0] for i in sim_scores]

    # Retornar o top 10 filmes mais similares
    return df['TITLE'].iloc[movie_indices]

# Testando a fun√ß√£o com alguns filmes para confirmar que tudo funciona como antes
print("Recomenda√ß√µes para 'Minions':")
print(get_recommendations('Minions'))
print("\nRecomenda√ß√µes para 'Interstellar':")
print(get_recommendations('Interstellar'))
print("\nRecomenda√ß√µes para 'Deadpool':")
print(get_recommendations('Deadpool'))

